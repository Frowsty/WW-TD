#follow the rabit hole, all classes run their calls to functions in game and main talks to game

def main():
    if _Fullscreen_flag == True:
        screen = pygame.display.set_mode((0,0), pygame.FULLSCREEN)
    else:
        screen = pygame.display.set_mode([round(SCREEN_WIDTH * _Multiplier),round(SCREEN_HEIGHT * _Multiplier)])

    pygame.display.set_caption('cowboy') #TODO make a title for the window
    pygame.mouse.set_visible(True)

    #create our objects and set data
    done = False
    all_Sprite_Group = pygame.sprite.Group()
    map = world_Map(screen)
    all_Sprite_Group.add(map)


    while not done:
        #process events
        pygame.event.pump()
        keyinput = pygame.key.get_pressed()
        mouse_Input = pygame.mouse.get_pressed()
        mouse_Pos = pygame.mouse.get_pos()
        all_Sprite_Group.events(keyinput, mouse_Input, mouse_Pos)
        #update stuff
        all_Sprite_Group.update()

        #draw
        all_Sprite_Group.draw(screen)

        #pause for the next frame

        FPSCLOCK.tick(60)


    pygame.quit() # closes window and exits

if __name__ == '__main__':
    main()


